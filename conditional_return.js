function isEven(num){
    if(num % 2 === 0){
        return true
    }
    else{
        return false
    }
}
console.log(isEven(5));
console.log(isEven(6));

function isOdd(num2){
    if(num2 % 2 !== 0){
        return true
    }
    else{
        return false
    }
}
console.log(isOdd(5));
console.log(isOdd(4));